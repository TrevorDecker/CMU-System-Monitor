<H3>What is this site used for? </H3>
</a>
<p>The purpose of this site is to provide usage information about computers in the various clusters at Carnegie Mellon University.</p>

<H3>
Who should use it?
</H3>
<p>This website is available to any CMU student with an andrewID who wants to quickly check for cluster availability on campus.</p>



<H3>How to use this website:</H3>
<p>
Click on the Find a Cluster tab at the top to be taken to a map showing all clusters on campuses. To find usage statistics and information about the computers in a specific cluster, simply click on the icon for the desired cluster in the main map.
</p>

<H3>
Current working features of this website:
</H3>
<p>Currently, the site only provides information for 4 of the Linux clusters on campus: Gates 3000, 5201, 5205 and 5208. Our service will extend to the Windows and OSX clusters in the near future.</p>


<H3>
Why was this site created?
</H3>
<p> The computer clusters on campus often experience extremely high traffic among students. Many students, including the creators of this website, have walked across campus to find an empty computer only to find none available upon arrival. We thought it would be much more convenient if people could check for free computers at home, saving valuable time in the process.</p>
<p>
This website also provides the resource usage statistics from ssh access of on-campus computers, providing students the ability to choose the most available, and therefore the fastest, computer.
</p>
